function StepPreview({ state }) {
  const yaml = generateYAML(state);
  const agentMd = generateAgentMd(state);
  const [copied, setCopied] = useState(false);
  const [tab, setTab] = useState("manifest");

  const copyToClipboard = (text) => {
    navigator.clipboard.writeText(text);
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  const currentContent = tab === "manifest" ? yaml : agentMd;

  return (
    <>
      <div style={{ display: "flex", gap: 8, marginBottom: 16 }}>
        <Chip active={tab === "manifest"} onClick={() => setTab("manifest")}>manifest.yaml</Chip>
        <Chip active={tab === "agent"} onClick={() => setTab("agent")}>agent.md</Chip>
      </div>
      <div style={{ position: "relative" }}>
        <pre
          style={{
            background: "#0a0a0b",
            border: "1px solid #222228",
            borderRadius: 10,
            padding: 20,
            fontFamily: "'JetBrains Mono', monospace",
            fontSize: 11.5,
            lineHeight: 1.7,
            color: "#c8ff00",
            overflowX: "auto",
            maxHeight: 440,
            whiteSpace: "pre-wrap",
            wordBreak: "break-word",
          }}
        >
          {currentContent}
        </pre>
        <button
          onClick={() => copyToClipboard(currentContent)}
          style={{
            position: "absolute",
            top: 10,
            right: 10,
            padding: "6px 14px",
            fontSize: 11,
            fontWeight: 600,
            background: copied ? "#c8ff0020" : "#16161a",
            border: "1px solid #333340",
            borderRadius: 6,
            color: copied ? "#c8ff00" : "#8888a0",
            cursor: "pointer",
          }}
        >
          {copied ? "Copied ✓" : "Copy"}
        </button>
      </div>
      <div style={{ marginTop: 16, display: "flex", gap: 12 }}>
        <button
          onClick={() => {
            const blob = new Blob([yaml], { type: "text/yaml" });
            const url = URL.createObjectURL(blob);
            const a = document.createElement("a");
            a.href = url;
            a.download = `${state.name || "agent"}-manifest.yaml`;
            a.click();
            URL.revokeObjectURL(url);
          }}
          style={{
            padding: "12px 24px",
            background: "#c8ff00",
            color: "#0a0a0b",
            border: "none",
            borderRadius: 8,
            fontSize: 14,
            fontWeight: 700,
            cursor: "pointer",
          }}
        >
          ↓ Download manifest.yaml
        </button>
        <button
          onClick={() => {
            const blob = new Blob([agentMd], { type: "text/markdown" });
            const url = URL.createObjectURL(blob);
            const a = document.createElement("a");
            a.href = url;
            a.download = "agent.md";
            a.click();
            URL.revokeObjectURL(url);
          }}
          style={{
            padding: "12px 24px",
            background: "transparent",
            color: "#e8e8ed",
            border: "1px solid #333340",
            borderRadius: 8,
            fontSize: 14,
            fontWeight: 600,
            cursor: "pointer",
          }}
        >
          ↓ Download agent.md
        </button>
      </div>
    </>
  );
}

export default function FylleBuilder() {
  const [step, setStep] = useState(0);
  const [state, setState] = useState(defaultState);

  const set = useCallback((key, val) => setState((prev) => ({ ...prev, [key]: val })), []);

  const currentStep = STEPS[step];
  const canNext = step < STEPS.length - 1;
  const canPrev = step > 0;

  const completionPct = useMemo(() => {
    let filled = 0;
    let total = 5;
    if (state.name) filled++;
    if (state.description) filled++;
    if (state.promptContent && state.promptContent.length > 30) filled++;
    if (state.inputs.some((i) => i.name)) filled++;
    if (state.toolsRequired.some((t) => t.name)) filled++;
    return Math.round((filled / total) * 100);
  }, [state]);

  const renderStep = () => {
    switch (currentStep.id) {
      case "identity": return <StepIdentity state={state} set={set} />;
      case "model": return <StepModel state={state} set={set} />;
      case "prompt": return <StepPrompt state={state} set={set} />;
      case "inputs": return <StepInputs state={state} set={set} />;
      case "tools": return <StepTools state={state} set={set} />;
      case "guardrails": return <StepGuardrails state={state} set={set} />;
      case "memory": return <StepMemory state={state} set={set} />;
      case "preview": return <StepPreview state={state} />;
      default: return null;
    }
  };

  return (
    <div style={{ minHeight: "100vh", background: "#0a0a0b", color: "#e8e8ed", fontFamily: "'Instrument Sans', system-ui, sans-serif" }}>
      {/* Header */}
      <div style={{ borderBottom: "1px solid #222228", padding: "16px 24px", display: "flex", justifyContent: "space-between", alignItems: "center" }}>
        <div style={{ display: "flex", alignItems: "center", gap: 12 }}>
          <Link to="/" style={{ fontFamily: "'JetBrains Mono', monospace", fontWeight: 700, color: "#c8ff00", fontSize: 16, textDecoration: "none" }}>Fylle</Link>
          <span style={{ color: "#333340" }}>|</span>
          <span style={{ color: "#55556a", fontSize: 13 }}>Builder</span>
        </div>
        <div style={{ display: "flex", alignItems: "center", gap: 16 }}>
          <div style={{ display: "flex", alignItems: "center", gap: 12 }}>
            <div style={{ width: 120, height: 4, background: "#222228", borderRadius: 2, overflow: "hidden" }}>
              <div style={{ width: `${completionPct}%`, height: "100%", background: "#c8ff00", borderRadius: 2, transition: "width 0.3s" }} />
            </div>
            <span style={{ fontSize: 11, color: "#55556a", fontFamily: "'JetBrains Mono', monospace" }}>{completionPct}%</span>
          </div>
          <a href="https://github.com/DavideScanta/fylle-format" target="_blank" rel="noopener noreferrer" style={{ color: "#8888a0", fontSize: 12, textDecoration: "none", fontWeight: 500, padding: "6px 12px", border: "1px solid #222228", borderRadius: 6, transition: "all 0.15s" }}>
            Spec ↗
          </a>
        </div>
      </div>

      <div style={{ display: "flex", minHeight: "calc(100vh - 57px)" }}>
        {/* Sidebar */}
        <div style={{ width: 200, borderRight: "1px solid #222228", padding: "16px 0", flexShrink: 0 }}>
          {STEPS.map((s, i) => (
            <button
              key={s.id}
              onClick={() => setStep(i)}
              style={{
                display: "flex",
                alignItems: "center",
                gap: 10,
                width: "100%",
                padding: "10px 20px",
                background: i === step ? "#c8ff0010" : "transparent",
                border: "none",
                borderRight: i === step ? "2px solid #c8ff00" : "2px solid transparent",
                color: i === step ? "#c8ff00" : i < step ? "#8888a0" : "#55556a",
                fontSize: 13,
                fontWeight: i === step ? 600 : 400,
                cursor: "pointer",
                textAlign: "left",
                fontFamily: "inherit",
                transition: "all 0.15s",
              }}
            >
              <span style={{ fontSize: 14, width: 20, textAlign: "center" }}>{s.icon}</span>
              {s.label}
            </button>
          ))}
        </div>

        {/* Main */}
        <div style={{ flex: 1, padding: "32px 40px", maxWidth: 800, overflowY: "auto" }}>
          <div style={{ marginBottom: 28 }}>
            <div style={{ fontSize: 11, fontFamily: "'JetBrains Mono', monospace", color: "#c8ff00", textTransform: "uppercase", letterSpacing: 2, marginBottom: 6 }}>
              Step {step + 1} of {STEPS.length}
            </div>
            <h2 style={{ fontSize: 24, fontWeight: 700, letterSpacing: -0.5 }}>{currentStep.label}</h2>
          </div>

          {renderStep()}

          {/* Nav buttons */}
          <div style={{ display: "flex", justifyContent: "space-between", marginTop: 32, paddingTop: 20, borderTop: "1px solid #222228" }}>
            <button
              onClick={() => canPrev && setStep(step - 1)}
              disabled={!canPrev}
              style={{
                padding: "10px 24px",
                background: "transparent",
                border: "1px solid #222228",
                borderRadius: 8,
                color: canPrev ? "#e8e8ed" : "#333340",
                fontSize: 13,
                fontWeight: 600,
                cursor: canPrev ? "pointer" : "default",
                fontFamily: "inherit",
              }}
            >
              ← Back
            </button>
            {canNext && (
              <button
                onClick={() => setStep(step + 1)}
                style={{
                  padding: "10px 24px",
                  background: "#c8ff00",
                  border: "none",
                  borderRadius: 8,
                  color: "#0a0a0b",
                  fontSize: 13,
                  fontWeight: 700,
                  cursor: "pointer",
                  fontFamily: "inherit",
                }}
              >
                Next →
              </button>
            )}
          </div>
        </div>
      </div>
    </div>
  );
}
