function StepInputs({ state, set }) {
  const addInput = () => set("inputs", [...state.inputs, { name: "", type: "text", required: false, description: "" }]);
  const removeInput = (i) => set("inputs", state.inputs.filter((_, idx) => idx !== i));
  const updateInput = (i, key, val) => {
    const next = [...state.inputs];
    next[i] = { ...next[i], [key]: val };
    set("inputs", next);
  };

  return (
    <>
      <Field label="Agent Inputs" sub="What the runtime must inject — makes the agent composable">
        {state.inputs.map((inp, i) => (
          <div key={i} style={{ display: "grid", gridTemplateColumns: "1fr 100px 80px 1fr 32px", gap: 8, alignItems: "center", marginBottom: 8 }}>
            <input style={monoInputStyle} placeholder="name" value={inp.name} onChange={(e) => updateInput(i, "name", e.target.value)} />
            <select style={{ ...inputStyle, padding: "10px 6px" }} value={inp.type} onChange={(e) => updateInput(i, "type", e.target.value)}>
              <option value="text">text</option>
              <option value="json">json</option>
              <option value="file">file</option>
              <option value="url">url</option>
            </select>
            <Chip active={inp.required} onClick={() => updateInput(i, "required", !inp.required)}>
              {inp.required ? "req" : "opt"}
            </Chip>
            <input style={inputStyle} placeholder="description" value={inp.description} onChange={(e) => updateInput(i, "description", e.target.value)} />
            {state.inputs.length > 1 && (
              <button onClick={() => removeInput(i)} style={{ background: "none", border: "none", color: "#ff6b8a", cursor: "pointer", fontSize: 16 }}>×</button>
            )}
          </div>
        ))}
        <SmallBtn onClick={addInput}>+ Add input</SmallBtn>
      </Field>
      <Field label="Output Format">
        <div style={{ display: "flex", flexWrap: "wrap", gap: 8 }}>
          {OUTPUT_FORMATS.map((f) => (
            <Chip key={f} active={state.outputFormat === f} onClick={() => set("outputFormat", f)}>{f}</Chip>
          ))}
        </div>
      </Field>
    </>
  );
}

function ToolList({ tools, setTools, label }) {
  const add = () => setTools([...tools, { name: "", protocol: "mcp", description: "", server: "" }]);
  const remove = (i) => setTools(tools.filter((_, idx) => idx !== i));
  const update = (i, key, val) => {
    const next = [...tools];
    next[i] = { ...next[i], [key]: val };
    setTools(next);
  };

  return (
    <Field label={label}>
      {tools.map((t, i) => (
        <div key={i} style={{ display: "grid", gridTemplateColumns: "1fr 90px 1fr 120px 32px", gap: 8, alignItems: "center", marginBottom: 8 }}>
          <input style={monoInputStyle} placeholder="tool name" value={t.name} onChange={(e) => update(i, "name", e.target.value)} />
          <select style={{ ...inputStyle, padding: "10px 6px" }} value={t.protocol} onChange={(e) => update(i, "protocol", e.target.value)}>
            {PROTOCOLS.map((p) => (
              <option key={p} value={p}>{p}</option>
            ))}
          </select>
          <input style={inputStyle} placeholder="description" value={t.description} onChange={(e) => update(i, "description", e.target.value)} />
          <input style={monoInputStyle} placeholder="server" value={t.server} onChange={(e) => update(i, "server", e.target.value)} />
          {tools.length > 0 && (
            <button onClick={() => remove(i)} style={{ background: "none", border: "none", color: "#ff6b8a", cursor: "pointer", fontSize: 16 }}>×</button>
          )}
        </div>
      ))}
      <SmallBtn onClick={add}>+ Add tool</SmallBtn>
    </Field>
  );
}

