function generateYAML(s) {
  const model = s.modelPreferred === "custom" ? s.modelCustom : s.modelPreferred;
  const caps = s.capabilities.map((c) => `\n      - "${c}"`).join("");
  
  let inputsBlock = "";
  const validInputs = s.inputs.filter((i) => i.name.trim());
  if (validInputs.length > 0) {
    inputsBlock = `\n  inputs:${validInputs
      .map(
        (i) =>
          `\n    - name: "${i.name}"\n      type: "${i.type}"\n      required: ${i.required}${i.description ? `\n      description: "${i.description}"` : ""}`
      )
      .join("")}`;
  }

  let toolsReqBlock = "";
  const validToolsReq = s.toolsRequired.filter((t) => t.name.trim());
  if (validToolsReq.length > 0) {
    toolsReqBlock = `\n    required:${validToolsReq
      .map(
        (t) =>
          `\n      - name: "${t.name}"\n        protocol: "${t.protocol}"${t.description ? `\n        description: "${t.description}"` : ""}${t.server ? `\n        server: "${t.server}"` : ""}`
      )
      .join("")}`;
  }

  let toolsOptBlock = "";
  const validToolsOpt = s.toolsOptional.filter((t) => t.name.trim());
  if (validToolsOpt.length > 0) {
    toolsOptBlock = `\n    optional:${validToolsOpt
      .map(
        (t) =>
          `\n      - name: "${t.name}"\n        protocol: "${t.protocol}"${t.description ? `\n        description: "${t.description}"` : ""}${t.server ? `\n        server: "${t.server}"` : ""}`
      )
      .join("")}`;
  }

  let toolsBlock = "";
  if (toolsReqBlock || toolsOptBlock) {
    toolsBlock = `\n  tools:${toolsReqBlock}${toolsOptBlock}`;
  }

  const validRules = s.guardrailsRules.filter((r) => r.trim());
  let guardrailsBlock = `\n  guardrails:\n    max_autonomy: "${s.maxAutonomy}"\n    limits:\n      max_iterations: ${s.maxIterations}\n      max_tokens_per_response: ${s.maxTokensPerResponse}`;
  if (validRules.length > 0) {
    guardrailsBlock += `\n    rules:${validRules.map((r) => `\n      - "${r}"`).join("")}`;
  }

  let memoryBlock = "";
  if (s.memoryEnabled) {
    const validFields = s.memoryFields.filter((f) => f.name.trim());
    memoryBlock = `\n  memory:\n    persistence: "${s.memoryPersistence}"`;
    if (validFields.length > 0) {
      memoryBlock += `\n    schema:${validFields
        .map(
          (f) =>
            `\n      - name: "${f.name}"\n        type: "${f.type}"${f.description ? `\n        description: "${f.description}"` : ""}`
        )
        .join("")}`;
    }
  }

  const tags = s.tags
    .split(",")
    .map((t) => t.trim())
    .filter(Boolean);
  const tagsBlock = tags.length > 0 ? `\n  tags: [${tags.map((t) => `"${t}"`).join(", ")}]` : "";

  return `# .fylle manifest — generated by Fylle Agent Builder
fylle_format: "0.1.0"

agent:
  name: "${s.name || "my-agent"}"
  version: "${s.version}"
  description: "${s.description}"${s.role ? `\n  role: "${s.role}"` : ""}

  author:
    name: "${s.authorName}"
  license: "${s.license}"

  model:
    preferred: "${model}"
    minimum_capability:${caps}
    settings:
      temperature: ${s.temperature}
      max_tokens: ${s.maxTokens}

  prompt_file: "agent.md"
${inputsBlock}
  output:
    format: "${s.outputFormat}"
${toolsBlock}
${guardrailsBlock}
${memoryBlock}
${tagsBlock}${s.category ? `\n  category: "${s.category}"` : ""}`;
}

